<template>
<div>
    <NavHeader/>
    <NavBread/>
    
    <div class="accessory-result-page accessory-page">
        <div class="container">
            <div class="filter-nav">
                <span class="sortby">Sort by:</span>
                <a href="javascript:void(0)" class="default cur">Default</a>
                <a href="javascript:void(0)" class="price" @click="sortGoods">价格 <svg class="icon icon-arrow-short"><use xlink:href="#icon-arrow-short"></use></svg></a>
                <a href="javascript:void(0)" class="filterby stopPop">Filter by</a>
            </div>
            <div class="accessory-result">
                <!-- filter -->
                <div class="filter stopPop" id="filter">
                    <dl class="filter-price">
                        <dt>Price:</dt>
                        <dd><a href="javascript:void(0)">All</a></dd>
                        <dd v-for="(item,index) in priceFilter" :key="index">
                            <a @click="setPriceFilter(index)" :class="{'cur':priceChecked == index}" href="javascript:void(0)">{{item.startPrice}} - {{item.endPrice}}</a>
                        </dd>
                        
                    </dl>
                </div>

                <!-- search result accessories list -->
                <div class="accessory-list-wrap">
                    <div class="accessory-list col-4">
                        <ul>
                            <li v-for="(item,index) in goods" :key="index">
                                <div class="pic">
                                    <a href="#"><img v-lazy="'/static/img/'+ item.productImage" alt=""></a>
                                </div>
                                <div class="main">
                                    <div class="name">{{item.productName}}</div>
                                    <div class="price">{{item.salePrice}}</div>
                                    <div class="btn-area">
                                        <a href="javascript:;" class="btn btn--m">加入购物车</a>
                                    </div>
                                </div>
                            </li>
                        </ul>
                    </div>
                </div>
            </div>
        </div>
    </div>

<NavFooter/>
    
</div>
    
</template>

<script>
    import NavHeader from '@/components/Header'
    import NavFooter from '@/components/Footer'
    import NavBread from '@/components/Bread'
    import axios from 'axios'
    // import '../../static/css/base.css'
    // import '../../static/css/product.css'
    export default{
        components:{
            NavHeader,
            NavFooter,
            NavBread
        },
        data(){
            return{
                goods:{},
                sortFlag:true,
                priceChecked:'all',
                data:[],
                busy: true,
                page:1,
                pageSize:8,
                priceFilter:[
                    {
                        startPrice:'0',
                        endPrice:'100'
                    },
                    {
                        startPrice:'100',
                        endPrice:'500'
                    },
                    {
                        startPrice:'500',
                        endPrice:'1000'
                    },
                    {
                        startPrice:'1000',
                        endPrice:'2000'
                    },
                ]
            }
        },
        created(){
            this.getGoodsList()
        },
        methods:{
           getGoodsList(flag){
            //    axios.get('http://easy-mock.com/mock/59664d4d58618039284c7710/example/goods/list').then(res=>{
            //         console.log(res);
            //         this.goods = res.data.data;
            //     }) 
            let sort = this.sortFlag ? 1 : -1;
            let param = {
                sort:sort,
                priceLevel:this.priceChecked,
                page:this.page,
                pageSize:this.pageSize
            }
            axios.get('/goods/list',{params:param}).then(res=>{
                if(flag){
                    this.goods = this.goods.concat(res.data.result);
                    if(res.data.result.length == 0){
                        this.busy = true;
                    }else{
                        this.busy = false;
                    }
                }else{
                    // 第一次加载数据
                    this.goods = res.data.result;
                    this.busy = false;
                }
            })
           },
           sortGoods(){
                this.sortFlag = !this.sortFlag;
                this.getGoodsList();
            },
            setPriceFilter(index){
               this.priceChecked = index;
               this.getGoodsList();
            },
            loadMore:function(){
                this.busy = true;
                setTimeout(()=>{
                    this.page ++;
                    this.getGoodsList(true);
                },1000);
            }
        }
    }
</script>
